"""
整数： 没有小数部分的数字，包含正整数 0 负整数
"""
age = 20
print(age)
print(type(age))

"""
浮点数（小数）
    小数通常以浮点数形式存储，浮点数和定点数是相对的：小数在存储过程中发生
    移动就是浮点数；存储过程中不发生移动就是定点数
"""
price = 22.1
print(price)
print(type(price))

"""
复数 python内置类型，直接书写即可。
    由实部和虚部构成，python中，复数的虚部j或者J作为后缀
    例如 a +bj
"""
person = 20 + 0.2j
print(person)
print(type(person))