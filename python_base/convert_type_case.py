"""
数据类型转换
int() 转换为整数
float() 转换为浮点数
complex() 转换复数
str() 转换字符串
repr() 转换为表达式字符串
eval() 计算字符串中的逻辑，返回一个对象
chr() 整数转换为字符串
ord() 字符转换整数值
hex() 整数转换十六进制字符串
oct() 整数转换8进制字符串
"""
print(int("1"))
print(float("1.0"))
print(str(1))
print(repr("aaa"))
print(eval("1+1"))
print(chr(20))
print(ord("30"))
print(hex(10))